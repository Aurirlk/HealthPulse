-- 药品订阅功能 - 数据库表结构
-- ============================================================

-- 药品表
CREATE TABLE IF NOT EXISTS `drug` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `name` VARCHAR(100) NOT NULL COMMENT '药品名称',
    `generic_name` VARCHAR(100) DEFAULT NULL COMMENT '通用名/化学名',
    `category` VARCHAR(50) NOT NULL COMMENT '药品分类（感冒药/消化系统/心脑血管/维生素/抗生素/外用药/中成药等）',
    `description` TEXT COMMENT '药品说明（功效、用法用量、不良反应、禁忌等）',
    `price` DECIMAL(10,2) NOT NULL COMMENT '价格（元）',
    `unit` VARCHAR(20) DEFAULT '盒' COMMENT '单位（盒/瓶/袋/片）',
    `specification` VARCHAR(100) DEFAULT NULL COMMENT '规格（如：0.5g×24片）',
    `manufacturer` VARCHAR(100) DEFAULT NULL COMMENT '生产厂家',
    `cover` VARCHAR(500) DEFAULT NULL COMMENT '药品图片',
    `is_otc` TINYINT(1) DEFAULT 1 COMMENT '是否OTC非处方药（1=是，0=处方药）',
    `stock` INT DEFAULT 100 COMMENT '库存数量',
    `status` TINYINT(1) DEFAULT 1 COMMENT '状态（1=上架，0=下架）',
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    INDEX `idx_category` (`category`),
    INDEX `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='药品信息表';

-- 用户药品订阅记录表
CREATE TABLE IF NOT EXISTS `drug_subscription` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `user_id` INT NOT NULL COMMENT '用户ID',
    `drug_id` INT NOT NULL COMMENT '药品ID',
    `quantity` INT DEFAULT 1 COMMENT '订阅数量',
    `create_time` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '订阅时间',
    INDEX `idx_user_id` (`user_id`),
    INDEX `idx_drug_id` (`drug_id`),
    FOREIGN KEY (`user_id`) REFERENCES `user`(`id`) ON DELETE CASCADE,
    FOREIGN KEY (`drug_id`) REFERENCES `drug`(`id`) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户药品订阅记录表';

